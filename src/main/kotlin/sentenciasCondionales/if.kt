package sentenciasCondionales

/**
 * Los condicionales son una de las bases más importantes de la programación. Son comandos en lenguajes de programación
 * que manejan decisiones. Con ellos, el código es dinámico, lo que significa que puede comportarse de manera diferente según
 * una condición diferente.
   En este codelab, aprenderás a usar las sentencias y expresiones if/else y when para escribir condicionales en Kotlin.
 */
// If-else: La expresión if-else se utiliza para tomar decisiones en función de una condición. Aquí tienes un ejemplo básico:
fun main (){
   val x = 10
   val y = 15
   if (x <y){
      println("x es menor que y")
   } else {
      println(" x no es menor que y")
   }

}